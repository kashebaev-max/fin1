// Получить контекст модуля по ключу
export function getModuleContext(moduleKey: string): ModuleContext | null {
  return MODULE_CONTEXTS[moduleKey] || null;
}

// Получить ключ модуля по pathname
export function getModuleKeyFromPath(pathname: string): string {
  if (pathname === "/dashboard" || pathname === "/dashboard/") return "dashboard";
  const match = pathname.match(/^\/dashboard\/([^/]+)/);
  return match ? match[1] : "dashboard";
}
