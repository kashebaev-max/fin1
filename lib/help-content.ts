export function getAllArticles(): { sectionKey: string; article: HelpArticle }[] {
  const result: { sectionKey: string; article: HelpArticle }[] = [];
  for (const section of HELP_SECTIONS) {
    for (const article of section.articles) {
      result.push({ sectionKey: section.key, article });
    }
  }
  return result;
}

export function findArticle(key: string): { section: HelpSection; article: HelpArticle } | null {
  for (const section of HELP_SECTIONS) {
    const article = section.articles.find(a => a.key === key);
    if (article) return { section, article };
  }
  return null;
}

export function searchArticles(query: string): { sectionKey: string; article: HelpArticle; matchIn: string }[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  const results: { sectionKey: string; article: HelpArticle; matchIn: string }[] = [];

  for (const section of HELP_SECTIONS) {
    for (const article of section.articles) {
      if (article.title.toLowerCase().includes(q)) {
        results.push({ sectionKey: section.key, article, matchIn: "title" });
        continue;
      }
      if (article.description.toLowerCase().includes(q)) {
        results.push({ sectionKey: section.key, article, matchIn: "description" });
        continue;
      }
      const stepMatch = article.steps.some(s =>
        s.title.toLowerCase().includes(q) || s.description.toLowerCase().includes(q)
      );
      if (stepMatch) {
        results.push({ sectionKey: section.key, article, matchIn: "steps" });
      }
    }
  }
  return results;
}
